import pandas as pd

df = pd.read_excel('警情列表_lingao_20241231-20260115_result_case.xlsx')
df['报警时间'] = pd.to_datetime(df['报警时间'])

# 查看2025年12月的数据
dec_2025 = df[(df['报警时间'] >= '2025-12-01') & (df['报警时间'] < '2026-01-01')]
print(f"2025年12月数据量: {len(dec_2025)}")

# 查看2025年11月的数据
nov_2025 = df[(df['报警时间'] >= '2025-11-01') & (df['报警时间'] < '2025-12-01')]
print(f"2025年11月数据量: {len(nov_2025)}")

# 查看2026年1月的数据
jan_2026 = df[(df['报警时间'] >= '2026-01-01') & (df['报警时间'] < '2026-02-01')]
print(f"2026年1月数据量: {len(jan_2026)}")

# 查看2024年12月的数据
dec_2024 = df[(df['报警时间'] >= '2024-12-01') & (df['报警时间'] < '2025-01-01')]
print(f"2024年12月数据量: {len(dec_2024)}")

# 查看按月分布
df['年月'] = df['报警时间'].dt.to_period('M')
print("\n=== 按月分布 ===")
print(df['年月'].value_counts().sort_index())
